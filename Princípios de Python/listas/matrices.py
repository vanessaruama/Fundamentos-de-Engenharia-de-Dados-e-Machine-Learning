matriz = [
    [1, "a", 2],
    ["b", 3, 4],
    [6, 5, "c"]
]

# Acessando elementos da matriz
print(matriz[0])
# Acessando o primeiro elemento da primeira linha
print(matriz[0][0])
# Acessando o último elemento da primeira linha
print(matriz[0][-1])
# Acessando o último elemento da última linha
print(matriz[-1][-1])
nome = "Vanessa Ruama"

# Fatiando a string para obter um caractere específico
print(nome[0])

# Fatiando a string para obter um intervalo de caracteres
print(nome[0:9])

# Fatiando a string para obter um intervalo de caracteres com um passo específico
print(nome[10:])

print(nome[10:16])

# Fatiando a string com uso de passo para obter caracteres em posições específicas e pular outros caracteres
print(nome[10:16:2])

# Fatiando a string sem especificar o índice de início e o índice de fim para obter a string completa
print(nome[:])

# Fatiando a string com um passo negativo para obter a string invertida
print(nome[::-1])
# Adição
print(1 + 1)

# Subtração
print(10 - 2)

# Multiplicação
print(5 * 3)  

# Divisão
print(10 / 2)

# Divisão inteira
print(10 // 3)

# Módulo (resto da divisão)
print(10 % 3)

# Exponenciação
print(2 ** 3)

# Ordem de precedência
print(10 - 5 * 2)

# Parênteses para alterar a ordem de precedência
print((10 - 5) * 2)

# Mais exemplos de ordem de precedência
print(10 ** 2 * 2)

# Exponenciação tem precedência sobre multiplicação e divisão
print(10 ** (2 * 2))

# Divisão e multiplicação têm a mesma precedência, então são avaliadas da esquerda para a direita
print(10 / 2 * 4)

#Exemplo de percentual
preco = 100
desconto = 20 / 100  # 20% de desconto
preco_com_desconto = preco * (1 - desconto)
print(preco_com_desconto)  # 80.0